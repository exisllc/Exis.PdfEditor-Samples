    public static async Task RunAsync()
    {
        ConsoleHelper.WriteHeader("Image Editor Demo");
        var outputDir = Path.Combine(Directory.GetCurrentDirectory(), "output");
        Directory.CreateDirectory(outputDir);

        try
        {
            // 1. Create a sample PDF with images on multiple pages
            ConsoleHelper.WriteInfo("Creating sample PDF with images...");
            var samplePath = Path.Combine(outputDir, "image-sample.pdf");

            byte[] logoJpeg = BuildSampleJpeg();

            var builder = PdfBuilder.Create();
            builder.AddPage(page =>
            {
                page.Size(PdfPageSize.A4);
                page.AddText("Company Report — Page 1", 72, 750, 18,
                    o => o.Bold().Color(0.1, 0.3, 0.8));
                page.AddImage(logoJpeg, x: 72, y: 500, width: 200, height: 150);
                page.AddText("Figure 1: Company Logo", 72, 480, 10);
            });
            builder.AddPage(page =>
            {
                page.Size(PdfPageSize.A4);
                page.AddText("Company Report — Page 2", 72, 750, 18,
                    o => o.Bold().Color(0.1, 0.3, 0.8));
                page.AddImage(logoJpeg, x: 300, y: 500, width: 150, height: 112);
                page.AddText("Figure 2: Logo (smaller)", 300, 480, 10);
            });
            builder.BuildToFile(samplePath);
            ConsoleHelper.WriteSuccess($"Sample PDF created: {samplePath}");

            // 2. Find all images
            ConsoleHelper.WriteInfo("\n--- Find Images ---");
            var sw = Stopwatch.StartNew();
            var found = await PdfImageEditor.FindImagesAsync(samplePath);
            sw.Stop();
            ConsoleHelper.WriteSuccess($"Found {found.TotalImages} unique image(s) across {found.PagesSearched} page(s) in {sw.ElapsedMilliseconds}ms");
            foreach (var img in found.Images)
            {
                ConsoleHelper.WriteInfo($"  Image #{img.Index}: {img.PixelWidth}x{img.PixelHeight} " +
                    $"{img.ColorSpace} {img.Format} — page(s) {string.Join(", ", img.PageNumbers)}");
            }

            // 3. Replace all images with a different JPEG
            ConsoleHelper.WriteInfo("\n--- Replace All Images ---");
            byte[] newLogo = BuildReplacementJpeg();
            var replacedPath = Path.Combine(outputDir, "image-replaced-all.pdf");
            sw.Restart();
            var replaceResult = await PdfImageEditor.ReplaceAllAsync(samplePath, replacedPath, newLogo);
            sw.Stop();
            ConsoleHelper.WriteSuccess($"Replaced {replaceResult.ImagesReplaced} of {replaceResult.ImagesFound} image(s) in {sw.ElapsedMilliseconds}ms");
            ConsoleHelper.WriteSuccess($"Output: {replacedPath}");

            // 4. Verify the replacement
            ConsoleHelper.WriteInfo("\n--- Verify Replacement ---");
            var verifyFound = await PdfImageEditor.FindImagesAsync(replacedPath);
            ConsoleHelper.WriteSuccess($"Replacement PDF has {verifyFound.TotalImages} image(s) across {verifyFound.PagesSearched} page(s)");

            ConsoleHelper.WriteSuccess("\nImage editor demo completed successfully!");
            ConsoleHelper.WriteInfo("Use cases: logo replacement, watermark updates, placeholder-to-real image swaps.");
        }
        catch (Exception ex)
        {
            ConsoleHelper.WriteError($"Image editor demo failed: {ex.Message}");
        }
    }

